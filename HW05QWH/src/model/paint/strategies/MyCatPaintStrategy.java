package model.paint.strategies;

/**
 * Paint strategy that uses my cats' photo.
 * 
 * @author Yuchang Shen and Weiheng Qiu
 *
 */
public class MyCatPaintStrategy extends ImagePaintStrategy {

	/**
	 * Constructor that load the cat image.
	 * @param filename
	 * 		The filename of the image file to use.
	 * @param fillFactor
	 * 		The ratio of the desired average diameter of the image to the actual average of the image's width and height.
	 */
	public MyCatPaintStrategy(String filename, double fillFactor) {
		super(filename, fillFactor);
	}

	/**
	 * Constructor that load the cat image.
	 */
	public MyCatPaintStrategy() {
		this("Image/mycats.jpg", 0.3);
	}

}
