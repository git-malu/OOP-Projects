package model.paint.shapes;

import java.awt.Shape;

/**
 * Interface for shape factories.
 * @author Yuchang Shen and Weiheng Qiu
 *
 */
public interface IShapeFac {

	/**
	 * 
	 * @param x x coordinate of the center location of the shape
	 * @param y y coordinate of the center location of the shape
	 * @param xScale The x-dimension of the shape, usually the x-radius.
	 * @param yScale The y-dimension of the shape, usually the y-radius.
	 * @return A shape
	 */
	public Shape makeShape(double x, double y, double xScale, double yScale);
}
