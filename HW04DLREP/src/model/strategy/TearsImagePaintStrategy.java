package model.strategy;

public class TearsImagePaintStrategy extends UprightImagePaintStrategy {
	/**
	 * File path for the image
	 * */
	private static String fileName = "../image/tears.png";

	/**
	 * Constructor that loads the Tears Emoji image
	 * */
	public TearsImagePaintStrategy() {
		super(fileName, 1);
	}

	/**
	 * Present the meaningful content on the GUI
	 * */
	public String toString() {
		return "TearsImage";
	}
}
